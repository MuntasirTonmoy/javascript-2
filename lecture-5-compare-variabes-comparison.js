//* operator

console.log(5 > 6); // is 5 greater than 6
console.log(5 < 6); // is 5 less than 6
console.log(5 == 6); // is 5 = 6
console.log(6 >= 6); // is 6 is greater or equal 6
console.log(6 <= 6); // is 6 is less or equal 6
console.log(5 != 6); // is 5 not equal 6

//* we can also compare variable 
var num1 = 10;
var num2 = 5;

console.log(num1 > num2);  // is num1 greater than num2
console.log(num1 < num2);
console.log(num1 == num2);
console.log(num1 >= num1);
console.log(num1 <= num1);
console.log(num1 != num2);

//* we can compare multiple statement by using && and || operator

console.log(num1 > num2 || num2 > num1);
console.log(num1 > num2 && num2 > num1);

